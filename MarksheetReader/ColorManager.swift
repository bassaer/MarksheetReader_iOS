import UIKit

class ColorManager {
    func mainColor() -> UIColor {
        return UIColor(red: 0, green: 80/255, blue: 127/255, alpha: 1.0)
    }
    
    func clearGrayColor() -> UIColor {
        return UIColor(red: 38/255, green: 38/255, blue: 38/255, alpha: 0.8)
    }
}
